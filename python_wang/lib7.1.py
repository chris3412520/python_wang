"""

依次输入10位评委的打分，存入列表，然后去掉最高分和最低分，计算并输出平均分。

 提取用户输入的字符串中的英文字母和数字，分别存放在两个列表中。要求英文字母不区分大小写，重复字母和数字只记一次。最后输出统计的所有英文字母和数字。

"""
"""根据输入的整数，输出特定表达式。

例如：在一行中输入两个1-9间 的整数a和n

          输出a+aa+aaa+aaaa+.......+aaaaaaaaa(n个a)"""

def print_a_n_times():
    """
    输入两个整数 a 和 n (1 <= a,n <= 9),
    输出形如: a + aa + aaa + ... 共 n 项.
    """
    a, n = map(int, input("请输入两个1~9之间的整数 a 和 n，用空格分开: ").split())
    # 生成表达式列表，比如 a=3, n=4 => ["3", "33", "333", "3333"]
    terms = [str(a) * i for i in range(1, n+1)]
    # 拼接成 "3 + 33 + 333 + 3333"
    expression = " + ".join(terms)

    print(expression)